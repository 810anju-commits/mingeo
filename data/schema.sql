CREATE TABLE IF NOT EXISTS applications (
    id INT AUTO_INCREMENT PRIMARY KEY,
    file_no VARCHAR(50) NOT NULL UNIQUE,
    applicant_name VARCHAR(150) NOT NULL,
    address TEXT,
    district VARCHAR(100),
    taluk VARCHAR(100),
    village VARCHAR(100),
    area VARCHAR(50),
    mineral_type VARCHAR(120),
    application_submission DATE,
    application_type VARCHAR(80),
    file_created DATE,
    proposed_scrutiny DATE,
    latest_document DATE,
    correction_letter DATE,
    proposed_inspection DATE,
    rectification_letter DATE,
    approved_plan DATE,
    forwarded_to_dmg DATE,
    release_execution DATE,
    first_permit DATE,
    statutory_license_submitted DATE,
    statutory_license_forwarded DATE,
    directorate_received DATE,
    directorate_scrutiny DATE,
    directorate_rectification DATE,
    loi_issued DATE,
    statutory_license_issued DATE,
    ql_order_issued DATE,
    ql_validity DATE,
    status VARCHAR(80) DEFAULT 'Under Processing',
    current_level VARCHAR(50) DEFAULT 'District',
    current_level_label VARCHAR(120) DEFAULT 'District Office',
    last_updated DATE,
    pending_reason VARCHAR(255),
    pending_with VARCHAR(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
